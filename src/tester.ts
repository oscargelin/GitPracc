let numOne: number = 55;
let numTwo: number = 87;

function addNumbers(numOne: number, numTwo: number): number {
  return numOne + numTwo;
}

function subtractNumbers(numOne: number, numTwo: number): number {
  return numOne - numTwo;
}

function multiplyNumbers(numOne: number, numTwo: number): number {
  return numOne * numTwo;
}

function divideNumbers(numOne: number, numTwo: number): number {
  return numOne / numTwo;
}

console.log(addNumbers(numOne, numTwo));
console.log(subtractNumbers(numOne, numTwo));
console.log(multiplyNumbers(numOne, numTwo));
console.log(divideNumbers(numOne, numTwo));
